package com.learn.statement;

public class StoredProcedure
{
	/*
	 * Callable statement  is used to call the stored procedure 
	 * which is present in the database
	 * 
	 * group of java code is needed repeatedly many time
	 * we just create one method
	 * wherever needed there we can call the method 
	 * 
	 * same as database programming
	 * some sql statements are needed repeatedly
	 * make these set of statements  as procedure
	 * wherever needed we can call the procedure
	 * that set of statements will be executed
	 * 
	 * 
	 * 
	 * STORED PROCEDURES: 
	 * 				reusable component 
	 * 				mysql --> Stored Procedure Language 
	 * 				group of sql queries which can perform some task
	 * 				procedures are stored inside the database permanently
	 * 				so only it is stored procedure
	 * 
	 * java method is responsible to write by programmer
	 * 
	 * Database admin have the rights to  write stored procedure
	 * not a java developer
	 * 
	 * 
	 * methods can take parameters like int x and int y
	 * same as methods
	 * stored procedure also having ability to take parameters
	 * 
	 * there are 3 types of parameters in stored procedures
	 * IN (provide input value)
	 * OUT(collect output values)
	 * IN OUT (provide input and collect output values)
	 * 
	 * 
	 */

}
